#!/usr/bin/env node

import { execFileSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const appRoot = path.resolve(scriptDir, "..");
const outputPath = path.join(appRoot, "src", "generated", "build-version.ts");

function readShortSha() {
  try {
    return execFileSync("git", ["rev-parse", "--short=7", "HEAD"], {
      cwd: appRoot,
      encoding: "utf8",
      stdio: ["ignore", "pipe", "ignore"],
    }).trim();
  } catch {
    return null;
  }
}

function formatKstStamp(date = new Date()) {
  const parts = new Intl.DateTimeFormat("en-US", {
    timeZone: "Asia/Seoul",
    month: "2-digit",
    day: "2-digit",
    hour: "2-digit",
    minute: "2-digit",
    hour12: false,
  }).formatToParts(date);
  const get = (type) => parts.find((part) => part.type === type)?.value ?? "00";
  return `${get("month")}${get("day")}-${get("hour")}${get("minute")}`;
}

const shortSha = readShortSha();
const buildVersion = shortSha ? `${shortSha} · ${formatKstStamp()}` : "dev";
const content = `// Generated by scripts/generate-build-version.mjs
// Do not edit by hand.

export const BUILD_VERSION = ${JSON.stringify(buildVersion)} as const;
`;

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, content);
console.log(`Generated ${path.relative(appRoot, outputPath)} (${buildVersion})`);
