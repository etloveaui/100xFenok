#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptPath = fileURLToPath(import.meta.url);
const scriptDir = path.dirname(scriptPath);
const appRoot = path.resolve(scriptDir, "..");
const repoRoot = path.resolve(appRoot, "..");
const defaultInputPath = path.join(repoRoot, "data", "admin", "fenok-data-health-kpi.json");
const defaultOutputPath = path.join(appRoot, "src", "generated", "lane-runid-map.ts");
const defaultPublicRoot = path.join(appRoot, "public");

function safeString(value) {
  return typeof value === "string" && value.trim() ? value.trim() : null;
}

function normalizeAttempt(attempt) {
  if (!attempt || typeof attempt !== "object" || Array.isArray(attempt)) return null;
  const runId = String(attempt.run_id ?? "").trim();
  if (!/^\d+$/.test(runId)) return null;
  const runAttempt = Number(attempt.run_attempt);
  return {
    run_id: runId,
    run_attempt: Number.isInteger(runAttempt) && runAttempt >= 1 ? runAttempt : null,
    event_name: safeString(attempt.event_name),
    observed_at: safeString(attempt.observed_at),
  };
}

export function buildLaneRunIdMap(payload) {
  const entries = [];
  for (const lane of Array.isArray(payload?.lanes) ? payload.lanes : []) {
    const laneId = safeString(lane?.id);
    if (!laneId) continue;
    const attempt = normalizeAttempt(lane?.details?.last_attempt)
      ?? normalizeAttempt(lane?.details?.current_attempt);
    if (attempt) entries.push([laneId, attempt]);
  }
  return Object.fromEntries(entries.sort(([first], [second]) => first.localeCompare(second)));
}

export function renderLaneRunIdModule(map) {
  return `import "server-only";

// Generated by scripts/generate-lane-runid-map.mjs from the private KPI doc.
// Do not edit by hand. Never copy this module under public/.

export type LaneRunIdEntry = {
  run_id: string;
  run_attempt: number | null;
  event_name: string | null;
  observed_at: string | null;
};

export const LANE_RUN_ID_MAP: Readonly<Record<string, LaneRunIdEntry>> = ${JSON.stringify(map, null, 2)};
`;
}

function isWithin(root, target) {
  const relative = path.relative(path.resolve(root), path.resolve(target));
  return relative === "" || (!relative.startsWith(`..${path.sep}`) && relative !== "..");
}

function publicFiles(root) {
  if (!fs.existsSync(root)) return [];
  return fs.readdirSync(root, { withFileTypes: true }).flatMap((entry) => {
    const absolute = path.join(root, entry.name);
    if (entry.isDirectory()) return publicFiles(absolute);
    return entry.isFile() ? [absolute] : [];
  });
}

export function assertNoPrivateRunIdsInPublic(publicRoot) {
  const leaks = [];
  for (const file of publicFiles(publicRoot)) {
    const contents = fs.readFileSync(file);
    const relative = path.relative(publicRoot, file);
    const text = contents.toString("utf8");
    const isPublicKpi = relative === path.join("data", "admin", "fenok-data-health-kpi.json");
    const isGeneratedRunIdMap = /lane-run-?id-map/i.test(path.basename(file))
      || text.includes("Generated by scripts/generate-lane-runid-map.mjs");
    if (isGeneratedRunIdMap || (isPublicKpi && /(["'])run_id\1\s*:/.test(text))) {
      leaks.push(path.relative(publicRoot, file));
    }
  }
  if (leaks.length > 0) throw new Error(`private lane run-id data leaked under public/: ${leaks.join(", ")}`);
  return true;
}

export function writeLaneRunIdMap({ map, outputPath, publicRoot }) {
  if (isWithin(publicRoot, outputPath)) {
    throw new Error(`lane run-id map must not be written under public/: ${outputPath}`);
  }
  assertNoPrivateRunIdsInPublic(publicRoot);
  fs.mkdirSync(path.dirname(outputPath), { recursive: true });
  fs.writeFileSync(outputPath, renderLaneRunIdModule(map));
  return outputPath;
}

function main() {
  const payload = JSON.parse(fs.readFileSync(defaultInputPath, "utf8"));
  const map = buildLaneRunIdMap(payload);
  writeLaneRunIdMap({ map, outputPath: defaultOutputPath, publicRoot: defaultPublicRoot });
  console.log(`Generated ${path.relative(appRoot, defaultOutputPath)} (${Object.keys(map).length} private lane run ids; public leak guard PASS)`);
}

if (process.argv[1] && path.resolve(process.argv[1]) === scriptPath) main();
