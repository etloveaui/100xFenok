import fs from "node:fs";
import path from "node:path";

const appRoot = process.cwd();
const routeManifestOutputPath = path.join(appRoot, "src", "generated", "static-route-manifest.ts");
const dataJsonManifestOutputPath = path.join(
  appRoot,
  "public",
  "generated",
  "data-json-files-manifest.json",
);

function collectHtmlFiles(root, { recursive }) {
  if (!fs.existsSync(root)) return [];

  const files = [];
  const stack = [root];

  while (stack.length > 0) {
    const current = stack.pop();
    if (!current) continue;

    for (const entry of fs.readdirSync(current, { withFileTypes: true })) {
      const absolutePath = path.join(current, entry.name);

      if (entry.isDirectory()) {
        if (recursive) stack.push(absolutePath);
        continue;
      }

      if (!entry.isFile() || !entry.name.endsWith(".html")) continue;

      const relativePath = path
        .relative(root, absolutePath)
        .split(path.sep)
        .join("/");
      files.push(relativePath);
    }
  }

  return files.sort((left, right) => right.localeCompare(left));
}

const postFiles = collectHtmlFiles(path.join(appRoot, "public", "posts-raw"), {
  recursive: true,
}).filter((file) => file !== "posts-main.html");

const travelFiles = collectHtmlFiles(
  path.join(appRoot, "public", "admin", "personal", "travel"),
  { recursive: false },
);

function collectJsonFilesByPath(dataRoot) {
  if (!fs.existsSync(dataRoot)) return {};

  const grouped = new Map();
  const stack = [dataRoot];

  while (stack.length > 0) {
    const current = stack.pop();
    if (!current) continue;

    for (const entry of fs.readdirSync(current, { withFileTypes: true })) {
      const absolutePath = path.join(current, entry.name);

      if (entry.isDirectory()) {
        stack.push(absolutePath);
        continue;
      }

      if (!entry.isFile() || !entry.name.endsWith(".json")) continue;

      const dirKey =
        path.relative(dataRoot, current).split(path.sep).join("/") || ".";
      const info = fs.statSync(absolutePath);
      const files = grouped.get(dirKey) ?? [];
      files.push({
        name: entry.name,
        sizeBytes: info.size,
        updatedAt: info.mtime.toISOString(),
      });
      grouped.set(dirKey, files);
    }
  }

  return Object.fromEntries(
    [...grouped.entries()]
      .map(([dirKey, files]) => [
        dirKey,
        files.sort((left, right) => left.name.localeCompare(right.name)),
      ])
      .sort(([left], [right]) => left.localeCompare(right)),
  );
}

const dataJsonFilesByPath = collectJsonFilesByPath(
  path.join(appRoot, "public", "data"),
);

const routeManifestContent = `// Generated by scripts/generate-static-route-manifest.mjs
// Do not edit by hand.

export const POST_HTML_FILES = ${JSON.stringify(postFiles, null, 2)} as const;

export const TRAVEL_HTML_FILES = ${JSON.stringify(travelFiles, null, 2)} as const;
`;

fs.mkdirSync(path.dirname(routeManifestOutputPath), { recursive: true });
fs.writeFileSync(routeManifestOutputPath, routeManifestContent);
fs.mkdirSync(path.dirname(dataJsonManifestOutputPath), { recursive: true });
fs.writeFileSync(
  dataJsonManifestOutputPath,
  `${JSON.stringify(dataJsonFilesByPath, null, 2)}\n`,
);
console.log(
  `Generated ${path.relative(appRoot, routeManifestOutputPath)} and ${path.relative(appRoot, dataJsonManifestOutputPath)} (${postFiles.length} posts, ${travelFiles.length} travel files, ${Object.values(dataJsonFilesByPath).reduce((total, files) => total + files.length, 0)} data json files)`,
);
