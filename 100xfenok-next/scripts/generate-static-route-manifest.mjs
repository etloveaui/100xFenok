import fs from "node:fs";
import path from "node:path";

const appRoot = process.cwd();
const outputPath = path.join(appRoot, "src", "generated", "static-route-manifest.ts");

function collectHtmlFiles(root, { recursive }) {
  if (!fs.existsSync(root)) return [];

  const files = [];
  const stack = [root];

  while (stack.length > 0) {
    const current = stack.pop();
    if (!current) continue;

    for (const entry of fs.readdirSync(current, { withFileTypes: true })) {
      const absolutePath = path.join(current, entry.name);

      if (entry.isDirectory()) {
        if (recursive) stack.push(absolutePath);
        continue;
      }

      if (!entry.isFile() || !entry.name.endsWith(".html")) continue;

      const relativePath = path
        .relative(root, absolutePath)
        .split(path.sep)
        .join("/");
      files.push(relativePath);
    }
  }

  return files.sort((left, right) => right.localeCompare(left));
}

const postFiles = collectHtmlFiles(path.join(appRoot, "public", "posts-raw"), {
  recursive: true,
}).filter((file) => file !== "posts-main.html");

const travelFiles = collectHtmlFiles(
  path.join(appRoot, "public", "admin", "personal", "travel"),
  { recursive: false },
);

function collectJsonEntries(root) {
  if (!fs.existsSync(root)) return [];

  return fs
    .readdirSync(root, { withFileTypes: true })
    .filter((entry) => entry.isFile() && entry.name.endsWith(".json") && entry.name !== "schema.json")
    .map((entry) => {
      const absolutePath = path.join(root, entry.name);
      const info = fs.statSync(absolutePath);
      return {
        name: entry.name,
        sizeBytes: info.size,
        updatedAt: info.mtime.toISOString(),
      };
    })
    .sort((left, right) => left.name.localeCompare(right.name));
}

const dataJsonManifestPaths = [
  "benchmarks",
  "sentiment",
  "slickcharts",
  "slickcharts/stocks",
  "damodaran",
  "macro",
  "sec-13f",
  "sec-13f/investors",
];

const dataJsonFilesByPath = Object.fromEntries(
  dataJsonManifestPaths.map((relativePath) => [
    relativePath,
    collectJsonEntries(path.join(appRoot, "public", "data", relativePath)),
  ]),
);

const content = `// Generated by scripts/generate-static-route-manifest.mjs
// Do not edit by hand.

export const POST_HTML_FILES = ${JSON.stringify(postFiles, null, 2)} as const;

export const TRAVEL_HTML_FILES = ${JSON.stringify(travelFiles, null, 2)} as const;

export const DATA_JSON_FILES_BY_PATH = ${JSON.stringify(dataJsonFilesByPath, null, 2)} as const;
`;

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, content);
console.log(
  `Generated ${path.relative(appRoot, outputPath)} (${postFiles.length} posts, ${travelFiles.length} travel files)`,
);
